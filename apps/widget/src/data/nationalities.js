// Convert ISO 3166-1 alpha-2 code to flag emoji
export function codeToFlag(code) {
  if (!code || code.length !== 2) return ''
  return String.fromCodePoint(
    ...code
      .toUpperCase()
      .split('')
      .map(c => 0x1f1e6 + c.charCodeAt(0) - 65)
  )
}

